package kr.co.himedia.abstractclass;
		// 모든 메서드가 구현된 클래스라도
		//↓ abstract 선언하면 추상클래스가 됨. 
public abstract class Computer2 {
	
	public void display() {}
	public void typing() {}
	
	public void turnOn() {
		System.out.println("전원을 켭니다.");
		
	}
	
	public void turnOff() {
		System.out.println("전원을 끕니다.");		
	}
	
	
	
	
	
	
	
	
	
}
